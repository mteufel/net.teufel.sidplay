package net.teufel.sidplay.domain;

public class Subtune {

    private int subtuneNo;
    private String length;

    public int getSubtuneNo() {
        return subtuneNo;
    }

    public void setSubtuneNo(int subtuneNo) {
        this.subtuneNo = subtuneNo;
    }

    public String getLength() {
        return length;
    }

    public void setLength(String length) {
        this.length = length;
    }
}
